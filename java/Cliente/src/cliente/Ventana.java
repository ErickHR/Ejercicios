    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        if(btnAgregar.getText().equals("AGREGAR")){
            estadosTextos(true);
            botones(btnAgregar.getText(), false);
            btnAgregar.setText("GUARDAR");
        }else{
           Persona usuario = new Persona(txtDireccion.getText(), txtDni.getText(), txtEmail.getText(), txtTelefono.getText(), txtNombre.getText());
        
            lista.insertarFinal(usuario);
            btnAgregar.setText("AGREGAR");
            estadosTextos(false);
            botones(btnAgregar.getText(), true);
            modificarTextos("", "", "", "", ""); 
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        // TODO add your handling code here:
        listaCliente = lista.getTop();
        
        modelo.setRowCount(0);
        int cantidad = 1;
        while( listaCliente != null ){
            datos[0] = Integer.toString(cantidad);
            datos[1] = listaCliente.getDni();
            datos[2] = listaCliente.getNombre();
            datos[3] = listaCliente.getEmail();
            datos[4] = listaCliente.getDireccion();
            datos[5] = listaCliente.getTelefono();
            
            modelo.addRow(datos);
            
            cantidad ++;
            
            listaCliente = listaCliente.getSiguiente();
            
        }
        
    }//GEN-LAST:event_btnListarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        if(lista.getCantidad()!=0){
            listaCliente = lista.getTop();
            if(btnModificar.getText().equals("MODIFICAR")){
                filaModificar = Integer.parseInt(JOptionPane.showInputDialog("Introduzca numero de la fila: "));
                if(!(filaModificar >= 0 && filaModificar <= lista.getCantidad())) return;
                estadosTextos(true);
                botones(btnModificar.getText(), false);
                int fila = 1;

                while(listaCliente != null){
                    if(fila == filaModificar) break;
                    fila++;
                    listaCliente = listaCliente.getSiguiente();

                }

                modificarTextos(listaCliente.getDireccion(), listaCliente.getDni(), 
                        listaCliente.getEmail(), listaCliente.getNombre(), listaCliente.getTelefono());

                btnModificar.setText("GUARDAR");

            }else{

                Nodo nuevo = new Nodo();

                nuevo.setDireccion(txtDireccion.getText());
                nuevo.setDni(txtDni.getText());
                nuevo.setEmail(txtEmail.getText());
                nuevo.setNombre(txtNombre.getText());
                nuevo.setTelefono(txtTelefono.getText());

                lista.modificar(filaModificar, nuevo);
                modificarTextos("", "", "", "", "");
                btnModificar.setText("MODIFICAR");
                estadosTextos(false);
                botones(btnModificar.getText(), true);

            }
        }else{
            JOptionPane.showMessageDialog(Ventana.this, "esta vacio!!!");
            btnModificar.setEnabled(false);
        }
        
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
 
        if(lista.getCantidad()!=0){
            
            int fila = Integer.parseInt(JOptionPane.showInputDialog("Introduzca numero de la fila: "));
            if(fila >= 1 && fila <= lista.getCantidad()){
                botones(btnEliminar.getText(), false);
                lista.eliminar(fila);
                botones(btnEliminar.getText(), true);
            }else JOptionPane.showMessageDialog(Ventana.this, "numero invalido");
            
        }else{
            
            JOptionPane.showMessageDialog(Ventana.this, "esta vacio!!!");
            btnEliminar.setEnabled(false);
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

